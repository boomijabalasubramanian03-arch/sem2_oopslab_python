mark = float(input("ENTER THE MARK OF THE STUDENT:"))
if mark>=91 and mark<=100:
   print("O GRADE")
elif mark>=81 and mark<=90:
   print("A+ GRADE")
elif mark>=71 and mark<=80:
   print("A GRADE")
elif mark>=61 and mark<=70:
   print("B+ GRADE")
elif mark>=51 and mark<=60:
   print("B GRADE")
else:
   print("RE-APPEAR")
